/**
 * Print : 1 2 3 4 5 6 7 8 9
 */

public class PrintCountingNumbersUsingWhile {
    public static void main(String[] args) {

        // //1. while loop
        int i = 1; // step 1 : initialise

        while (i <= 9) { // step 2 : condition check

            System.out.println(i); // task

            i++; // or i = i + 1 ; Step 3 : increament / decrement
        }

    }
}
